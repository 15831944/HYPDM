        public LogisticApprovalForm()
        {
            InitializeComponent();
            this.IsMdiContainer = true;
        }


        private void dataGridView1_CellMouseClick(object sender, DataGridViewCellMouseEventArgs e)
        {
            if (e.Button == MouseButtons.Right)
            {
                if (e.RowIndex >= 0)
                {
                    //若行已是选中状态就不再进行设置
                    if (dataGridView1.Rows[e.RowIndex].Selected == false)
                    {
                        dataGridView1.ClearSelection();
                        dataGridView1.Rows[e.RowIndex].Selected = true;
                    }
                    
                    //只选中一行时设置活动单元格
                    if (dataGridView1.SelectedRows.Count == 1)
                    {
                        dataGridView1.CurrentCell = dataGridView1.Rows[e.RowIndex].Cells[e.ColumnIndex];
                    }
                    //弹出操作菜单
                    contextMenuStrip1.Show(MousePosition.X, MousePosition.Y);
                }
            }
        }

        private void productStructMenuItem_Click(object sender, EventArgs e)
        {
            Form useMsgForm = new parts.UseMsgForm();
            useMsgForm.StartPosition = FormStartPosition.CenterParent;
            useMsgForm.ShowDialog();
            useMsgForm.Focus();
        }

    }
